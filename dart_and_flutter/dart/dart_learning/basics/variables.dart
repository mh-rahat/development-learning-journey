// Variables are used for storing a particular value.

void main() {
  mainString();
  mainInt();
  mainPoint();
  mainBoolean();
}

// variable of string value
void mainString() {
  var subject1 = "Physics ";
  var subject2 = "Higher Math ";
  var subject3 = "Chemistry ";
  var subject4 = "Biology ";

  print('variable of string value');
  print(subject1 + subject2 + subject3 + subject4);
  print('\n');
}

// variable of int value
void mainInt() {
  var rahim = 20;
  var karim = 30;
  var sum = rahim + karim;

  print('variable of int value');
  print(sum);
  print('\n');
}

// variable of double value
void mainPoint() {
  var grade1 = 4.56;
  var grade2 = 5.00;
  var grade3 = 3.98;

  print('variable of double value');
  print(grade1);
  print(grade2);
  print(grade3);
  print('\n');
}

// variable of boolean value
void mainBoolean() {
  var bool1 = true;
  var bool2 = false;

  print('variable of boolean value');
  print(bool1);
  print(bool2);
}
